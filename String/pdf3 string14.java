class Solution
{
    //Function to find the first non-repeating character in a string.
    static char nonrepeatingCharacter(String S)
    {
        int length=S.length();
        for(int i=0;i<length;i++){
            char t=S.charAt(i);
            if(S.indexOf(t)==S.lastIndexOf(t)){
                return t;
            }
        }
        return '$';
    }
}


